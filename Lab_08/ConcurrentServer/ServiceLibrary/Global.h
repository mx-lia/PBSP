struct Contact
{
	enum TE
	{
		EMPTY,
		ACCEPT,
		CONTACT
	} type;

	enum ST
	{
		WORK,
		ABORT,
		TIMEOUT,
		FINISH
	} sthread;

	SOCKET s;
	SOCKADDR_IN prms;
	int lprms;
	HANDLE hthread;
	HANDLE htimer;
	bool TimerOff;
	bool CloseConn;

	char msg[50];
	char srvname[15];

	HANDLE hAcceptServer;

	Contact(TE t = EMPTY, const char* namesrv = "")
	{
		memset(&prms, 0, sizeof(SOCKADDR_IN));

		lprms = sizeof(SOCKADDR_IN);
		type = t;             
		
		strcpy(srvname,namesrv);  
		
		msg[0] = 0;

		CloseConn = false;
		TimerOff = false;
	};            

	void SetST(ST sth, const char* m = "" ) 
	{
		sthread = sth;     
		strcpy(msg, m);
	}           
};

